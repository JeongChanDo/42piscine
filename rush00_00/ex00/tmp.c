#include <unistd.h>
void ft_putchar(int k);

void ft_rush00(int col, int row)
{
	int i;
	int j;
	i = 0;
	while (i < row)
	{
		j = 0;
		while (j < col)
		{
			if ((i == 0 && j == 0) || (i == 0 && j == col - 1) || (i == row - 1 && j == 0) || (i == row - 1 && j == col - 1))
			{
				ft_putchar('o');
			}
			else if (i == 0 || i == row - 1)
			{
				ft_putchar('-');
			}
			else if (j == 0 || j == col - 1)
			{
				ft_putchar('|');
			}
			else
			{
				ft_putchar(' ');
			}
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}

void main(){
	ft_rush00(5,5);
}
